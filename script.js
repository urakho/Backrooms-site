function article(id, number, name, grade, summary, tags, sections, images = [], imageNote = "") {
  return {
    id,
    number,
    name,
    danger: grade <= 1 ? "safe" : grade === 2 ? "warning" : grade === 3 ? "unknown" : "hostile",
    grade: String(grade),
    state: classState(grade),
    statuses: statusList(grade),
    summary,
    tags,
    sections,
    images,
    imageNote
  };
}

function classState(grade) {
  if (grade === 0) return "Безопасен";
  if (grade === 1) return "Низкая опасность";
  if (grade === 2) return "Умеренная опасность";
  if (grade === 3) return "Нестабильная опасность";
  if (grade === 4) return "Высокая опасность";
  return "Критическая опасность";
}

function statusList(grade) {
  if (grade === 0) return ["Безопасен", "Нестабилен", "Нет сущностей"];
  if (grade === 1) return ["Безопасен", "Стабилен", "Небольшое количество сущностей"];
  if (grade === 2) return ["Умеренно опасен", "Частично стабилен", "Возможны сущности"];
  if (grade === 3) return ["Нестабилен", "Требует осторожности", "Сущности возможны"];
  if (grade === 4) return ["Опасен", "Нестабилен", "Враждебные сущности вероятны"];
  return ["Крайне опасен", "Сильно нестабилен", "Высокая активность угроз"];
}

function allItems() {
  return categoryOrder.flatMap((category) =>
    siteSections[category].items.map((item) => ({ ...item, category }))
  );
}

function getItemById(id) {
  return allItems().find((item) => item.id === id) || allItems()[0];
}

function escapeHtml(value) {
  return String(value)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#039;");
}

function assetUrl(path) {
  const encodedPath = path
    .split("/")
    .map((part) => encodeURIComponent(part))
    .join("/");

  return `${encodedPath}?v=${appVersion}`;
}

