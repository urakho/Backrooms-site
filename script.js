function article(id, number, name, grade, summary, tags, sections) {
  return {
    id,
    number,
    name,
    danger: grade <= 1 ? "safe" : grade === 2 ? "warning" : grade === 3 ? "unknown" : "hostile",
    grade: String(grade),
    state: classState(grade),
    statuses: statusList(grade),
    summary,
    tags,
    sections
  };
}

function classState(grade) {
  if (grade === 0) return "Безопасен";
  if (grade === 1) return "Низкая опасность";
  if (grade === 2) return "Умеренная опасность";
  if (grade === 3) return "Нестабильная опасность";
  if (grade === 4) return "Высокая опасность";
  return "Критическая опасность";
}

function statusList(grade) {
  if (grade === 0) return ["Безопасен", "Нестабилен", "Нет сущностей"];
  if (grade === 1) return ["Безопасен", "Стабилен", "Небольшое количество сущностей"];
  if (grade === 2) return ["Умеренно опасен", "Частично стабилен", "Возможны сущности"];
  if (grade === 3) return ["Нестабилен", "Требует осторожности", "Сущности возможны"];
  if (grade === 4) return ["Опасен", "Нестабилен", "Враждебные сущности вероятны"];
  return ["Крайне опасен", "Сильно нестабилен", "Высокая активность угроз"];
}

function allItems() {
  return порядокРазделов.flatMap((category) =>
    разделы[category].items.map((item) => ({ ...item, category }))
  );
}

function getItemById(id) {
  return allItems().find((item) => item.id === id) || allItems()[0];
}

function escapeHtml(value) {
  return String(value)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#039;");
}

function assetUrl(path) {
  return path
    .split("/")
    .map((part) => encodeURIComponent(part))
    .join("/");
}

function itemSearchText(item) {
  return [
    item.number,
    item.name,
    item.state,
    item.summary,
    ...item.tags,
    ...item.statuses,
    ...item.sections.flatMap((sectionItem) => [sectionItem.title, ...sectionItem.paragraphs])
  ]
    .join(" ")
    .toLowerCase();
}

function matches(item, query) {
  if (!query) return true;
  return itemSearchText(item).includes(query.toLowerCase());
}

function pluralize(count) {
  const mod10 = count % 10;
  const mod100 = count % 100;
  if (mod10 === 1 && mod100 !== 11) return "запись";
  if (mod10 >= 2 && mod10 <= 4 && (mod100 < 12 || mod100 > 14)) return "записи";
  return "записей";
}

function classLevel(item) {
  if (item.grade === "UNC") return "UNC";
  return item.grade === "Q" ? "Q" : Number(item.grade);
}

function classBadge(item) {
  if (item.grade === "UNC") return "Класс выживания: Неподтверждённый";
  if (item.grade === "Q") return "Класс выживания: Не определён";
  return `Класс выживания: ${classLevel(item)}`;
}

function classPill(item) {
  if (item.grade === "UNC") return "Класс выживания: Неподтверждённый";
  if (item.grade === "Q") return "Класс выживания: Не определён";
  return `Класс выживания: ${classLevel(item)}`;
}

function difficultyImage(item) {
  if (item.grade === "UNC") return "assets/diff-UNC.png";
  if (item.grade === "Q") return "assets/diff-Q.png";
  const grade = classLevel(item);
  if (grade >= 0 && grade <= 5) {
    return `assets/diff-${grade}.png`;
  }
  return "";
}

function renderDifficultyImage(item) {
  const src = difficultyImage(item);
  if (!src) return "";

  return `<img class="difficulty-icon" src="${escapeHtml(assetUrl(src))}" alt="${escapeHtml(classPill(item))}" onerror="this.remove()" />`;
}

function renderGallery(item) {
  if (!item.images || !item.images.length) return "";

  return `
    <div class="article-gallery">
      ${item.images
        .map(
          (image) => `
            <figure>
              <img src="${escapeHtml(assetUrl(image.src))}" alt="${escapeHtml(image.alt)}" onerror="this.closest('figure').classList.add('image-missing')" />
              <figcaption>${escapeHtml(image.caption)}</figcaption>
            </figure>
          `
        )
        .join("")}
    </div>
  `;
}

function renderStatusBlock(item, category) {
  return `
    <p><strong>Раздел:</strong> ${escapeHtml(разделы[category].shortTitle)}</p>
    <p><strong>${escapeHtml(classPill(item))}</strong></p>
    ${item.statuses.map((status) => `<p><strong>${escapeHtml(status)}</strong></p>`).join("")}
  `;
}

function renderCards() {
  const query = searchInput.value.trim();
  const category = разделы[activeCategory];
  const items = category.items.filter((item) => matches(item, query));

  categoryTitle.textContent = category.title;
  resultCount.textContent = `${items.length} ${pluralize(items.length)}`;

  if (!items.length) {
    cards.innerHTML = `
      <article class="card empty-state">
        <h3>Ничего не найдено</h3>
        <p>Попробуй другой запрос или переключи раздел архива.</p>
      </article>
    `;
    return;
  }

  cards.innerHTML = items
    .map(
      (item) => `
        <article class="card">
          <div class="card-top">
            <span class="number">${escapeHtml(item.number)}</span>
            <span class="danger class-${classLevel(item)}">${escapeHtml(classBadge(item))}</span>
          </div>
          <h3>${escapeHtml(item.name)}</h3>
          <p>${escapeHtml(item.summary)}</p>
          <a class="open-article" href="article.html?id=${encodeURIComponent(item.id)}">Открыть статью</a>
        </article>
      `
    )
    .join("");
}

function renderArticle() {
  const item = getItemById(activeArticleId);
  document.title = `${item.number} - ${item.name} | Архив Закулисья`;

  articlePage.innerHTML = `
    <header class="article-header">
      <div class="article-meta">
        <span class="doc-code">${escapeHtml(item.number)}</span>
        <span class="state-pill class-${classLevel(item)}">${escapeHtml(classPill(item))}</span>
      </div>
      <h2>${escapeHtml(item.name)}</h2>
      <p class="article-subtitle">${escapeHtml(item.summary)}</p>
    </header>

    <section class="survival-box" aria-label="Классификация">
      <div class="survival-grade class-${classLevel(item)}">
        ${renderDifficultyImage(item)}
      </div>
      <div class="survival-copy">
        ${renderStatusBlock(item, item.category)}
      </div>
    </section>

    <div class="article-body">
      ${renderGallery(item)}
      <nav class="toc" aria-label="Разделы статьи">
        ${item.sections.map((sectionItem) => `<span>${escapeHtml(sectionItem.title)}</span>`).join("")}
      </nav>

      ${item.sections
        .map(
          (sectionItem) => `
            <section class="article-section">
              <h3>${escapeHtml(sectionItem.title)}</h3>
              ${sectionItem.paragraphs.map((paragraph) => `<p>${escapeHtml(paragraph)}</p>`).join("")}
            </section>
          `
        )
        .join("")}

    </div>
  `;
}

